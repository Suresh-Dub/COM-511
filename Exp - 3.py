# Iterating over a List
fruits = ["apple", "banana", "cherry", "date"]

print("Iterating over a List:")
for fruit in fruits:
    print(fruit)

# Iterating over a Dictionary
student_info = {
    "name": "John",
    "age": 20,
    "grade": "A",
    "city": "New York"
}

print("\nIterating over a Dictionary:")
for key, value in student_info.items():
    print(f"{key}: {value}")
